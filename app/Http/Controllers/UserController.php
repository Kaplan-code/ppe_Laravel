    {
        $password = bcrypt($request->password);
        DB::table('users')->insert(['name' => $request->name, 'email' => $request->email, 'password' => $password , 'role_id' => $request->role_id]);
        return redirect()->route('users.index')->with('success', 'La demande a bien été ajouté.');
    }

